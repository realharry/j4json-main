package j4json.common;


/**
 * Predefined JSON string literals.
 */
public final class Literals
{
    private Literals() {}
    
    public static final String NULL = "null";
    public static final String TRUE = "true";
    public static final String FALSE = "false";
    public static final int NULL_LENGTH = NULL.length();
    public static final int TRUE_LENGTH = TRUE.length();
    public static final int FALSE_LENGTH = FALSE.length();

}
